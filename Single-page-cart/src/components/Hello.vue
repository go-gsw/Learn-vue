import axios from 'axios'
export default {
  name: 'hello',
  data () {
    return {
      msg: '',
      title:'',
      cartList:[],
      totalMoney:0,
      finalTotal:0,
      // checkAll:"checked",
      ischeckAll:'',
      isDelete: false,
      curProduct: ''
      // title: 'hello,vue'
    }
  },
  filters:{
    //过滤器
    formatMoney:function(value){
      //前端toFixed会有精度丢失
      //数据应该由后端返回带精度值
      return "￥"+value.toFixed(2);
    }
  },
  mounted:function(){
    this.$nextTick(function(){
      this.cartView();
    })
  },
  methods:{
    cartView:function(){
      // 为了减少作用域链的搜索，设定一个局部变量接受this.
      //chrome浏览器下的webkit内核考虑安全因素禁用正向反向缓存。
      //response被重新封装。
      let _this=this;
      //箭头函数作用域已经指向了外层。
      axios.get("static/resource/cart.json").then((res)=>{
        console.log(res);
        this.title=res.data.title;
        this.msg = res.data.message;
        this.cartList =res.data.result.list;
        this.totalMoney =res.data.result.totalMoney;
      }).catch( err =>{
        // catch方法用于捕捉程序的异常，catch和errorCallback是不同的，
        // errorCallback只是在响应失败的时候调用，而catch是在整个请求到响应的
        // 过程中，只要程序出现错误就会被调用。
        console.log(err);
      });
    },
    changeMoney:function(cart,way){
      if (way>0) {
        cart.count++;
      }else{
        cart.count--;
        if(cart.count<1){
          cart.count=1;
        }
      }
      this.calcfinalTotal();
    },
    select:function(item){
      if(typeof item.checked=='undefined'){
        // Vue.set(item,"checked",true);
        this.$set(item,"checked",true);
      }else{
        item.checked =!item.checked;
      }
      this.calcfinalTotal();
    },
    checkall:function(flag){
      this.ischeckAll =flag;
        this.cartList.forEach((item,index)=>{
          if(typeof item.checked=='undefined'){
          this.$set(item,"checked",this.ischeckAll);
          //this.$set(item,"checked",true);
          }else{
            item.checked=this.ischeckAll;
          }
        });
        this.calcfinalTotal();
    },
    calcfinalTotal:function(){
      this.finalTotal = 0;
      this.cartList.forEach((item,index) => {
        if( item.checked ){
          this.finalTotal += item.price*item.count;
        }
      });
    },
    deleteConfirm:function(item){
      this.isDelete = true;
      this.curProduct = item;
    },
    deleteCart:function(){
      let index = this.cartList.indexOf(this.curProduct);
      this.cartList.splice(index,1);
      this.isDelete = false;
    },
    checkoutAll(){
      console.log(this.finalTotal)
      localStorage.setItem('allMonery',JSON.stringify(this.finalTotal));
      this.$router.push('/address')
    }
  }
}
</script>

